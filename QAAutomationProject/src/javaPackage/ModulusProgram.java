package javaPackage;

public class ModulusProgram {

	public static void main(String[] args) {
		
		int a =50;
		int b=10;
		// Modulus sign will give reminder after division that means if we divide 50/10 it will give reminder 0
		int mod=a%b;
		System.out.println("Print the reminder "+mod);

	}

}
